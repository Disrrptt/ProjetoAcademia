    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //associar
        try {
            int idAluno = Integer.parseInt(jTextField1.getText());
            int idTreino = Integer.parseInt(jTextField2.getText());

            Map<String, String> resultado = academiaController.associarTreinoAoAluno(idAluno, idTreino);

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.addRow(new Object[]{resultado.get("alunoNome"), resultado.get("treinoTipo")});

            JOptionPane.showMessageDialog(this, "Treino associado ao aluno com sucesso!");

            // Abre o novo JFrame
            ListarTreinosDeAlunos listarTreinosDeAlunos = new ListarTreinosDeAlunos();
            listarTreinosDeAlunos.setVisible(true);

        } catch (NumberFormatException | SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao associar treino ao aluno. Certifique-se de inserir IDs válidos.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

