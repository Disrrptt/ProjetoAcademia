    private void atualizarAluno() {
        int linha = jTable1.getSelectedRow();
        if (linha > -1) {
            Aluno aluno = alunos.get(linha);

            // Solicita novos valores ao usuário
            String novoNome = JOptionPane.showInputDialog(this, "Novo nome:", aluno.getNome());
            String novoGenero = JOptionPane.showInputDialog(this, "Novo gênero:", aluno.getGenero());
            String novoEndereco = JOptionPane.showInputDialog(this, "Novo endereço:", aluno.getEndereco());
            String novoTelefone = JOptionPane.showInputDialog(this, "Novo telefone:", aluno.getTelefone());
            String novoEmail = JOptionPane.showInputDialog(this, "Novo email:", aluno.getEmail());

            // Atualiza os dados do aluno
            aluno.setNome(novoNome);
            aluno.setGenero(novoGenero);
            aluno.setEndereco(novoEndereco);
            aluno.setTelefone(novoTelefone);
            aluno.setEmail(novoEmail);

            // Chama o método para atualizar no banco de dados
            AlunoController alunoController = new AlunoController();
            alunoController.atualizarAluno(aluno);

            JOptionPane.showMessageDialog(this, "Aluno atualizado com sucesso!");
            montarListaAlunos();
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um aluno para editar!");
        }
    }

    private void excluirAluno() {
        int linha = jTable1.getSelectedRow();
        if (linha > -1) {
            Aluno aluno = alunos.get(linha);
            int opcao = JOptionPane.showConfirmDialog(this,
                    "Deseja realmente excluir o aluno " + aluno.getNome() + "?",
                    "Confirme a exclusão", JOptionPane.YES_NO_OPTION);

            if (opcao == JOptionPane.YES_OPTION) {
                AlunoController alunoController = new AlunoController();
                alunoController.excluirAluno(aluno.getId());
                JOptionPane.showMessageDialog(this, "Aluno excluído com sucesso!");
                montarListaAlunos();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um aluno para excluir!");
        }
    }

