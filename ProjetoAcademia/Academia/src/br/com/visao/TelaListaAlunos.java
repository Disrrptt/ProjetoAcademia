    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int linha = jTable1.getSelectedRow();
        if (linha > -1) {
            Aluno aluno = alunos.get(linha);
            int opcao = JOptionPane.showConfirmDialog(this,
                    "Deseja realmente excluir o aluno " + aluno.getNome() + "?",
                    "Confirme a exclusão", JOptionPane.YES_NO_OPTION);

            if (opcao == JOptionPane.YES_OPTION) {
                AlunoController alunoController = new AlunoController();
                alunoController.excluirAluno(aluno.getId());
                JOptionPane.showMessageDialog(this, "Aluno excluído com sucesso!");
                montarListaAlunos();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um aluno para excluir!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

